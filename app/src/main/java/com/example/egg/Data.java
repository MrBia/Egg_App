package com.example.egg;

public class Data {
    String day;
    String time;
    String temperature;
    String numberEggHatches;
    String numberEgg;

    public String getDay() {
        return day;
    }

    public String getTime() {
        return time;
    }

    public String getTemperature() {
        return temperature;
    }

    public String getNumberEggHatches() {
        return numberEggHatches;
    }

    public String getNumberEgg() {
        return numberEgg;
    }

    public void setDay(String day) {
        this.day = day;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public void setTemperature(String temperature) {
        this.temperature = temperature;
    }

    public void setNumberEggHatches(String numberEggHatches) {
        this.numberEggHatches = numberEggHatches;
    }

    public void setNumberEgg(String numberEgg) {
        this.numberEgg = numberEgg;
    }
}
